
CREATE TABLE Customers (
	CustomerID CHAR(5) NOT NULL, 
	CompanyName STRING NOT NULL, 
	ContactName STRING NULL, 
	ContactTitle STRING NULL, 
	Address STRING NULL, 
	City STRING NULL, 
	Region STRING NULL, 
	PostalCode STRING NULL, 
	Country STRING NULL, 
	Phone STRING NULL, 
	Fax STRING NULL, 
	PRIMARY KEY (CustomerID)
)
